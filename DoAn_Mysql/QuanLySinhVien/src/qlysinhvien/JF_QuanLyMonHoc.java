    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        Boolean ketQua = true;
        DefaultTableModel model = (DefaultTableModel) table_info.getModel();
        try {
            MonHoc mh = new MonHoc(txtMaMH.getText(), txtTenMonHCoc.getText(), Integer.parseInt(txtSOTiet.getText()));
            for (int i = 0; i < model.getRowCount(); i++) {
                if (model.getValueAt(i, 1).equals(txtMaMH.getText())) {
                    ketQua = false;
                    break;
                }
            }
            if (ketQua == false) {
                JOptionPane.showMessageDialog(this, "Trùng Mã Môn Học");
            } else {
                list.themMonHoc(mh);
            }
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

