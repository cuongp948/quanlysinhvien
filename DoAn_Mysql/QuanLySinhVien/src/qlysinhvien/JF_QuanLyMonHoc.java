    private void table_infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_infoMouseClicked
        // TODO add your handling code here:
        int dong = table_info.getSelectedRow();
        String[] arr = new String[]{
            table_info.getValueAt(dong, 0).toString(), table_info.getValueAt(dong, 1).toString(), table_info.getValueAt(dong, 2).toString()
        };
        txtMaMH.setText(arr[0]);
        txtTenMonHCoc.setText(arr[1]);
        txtSOTiet.setText(table_info.getValueAt(dong, 2).toString());
    }//GEN-LAST:event_table_infoMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        DefaultTableModel dtmodel = (DefaultTableModel) table_info.getModel();
         if (btnSua.getText() == "Sửa") {
            txtMaMH.setEnabled(false);
               btnSua.setText("Hủy Chế Độ Sửa");
        } else {
        if (table_info.getSelectedRowCount() > 0) {
            MonHoc mh;
            try {
                mh = new MonHoc(txtMaMH.getText(), txtTenMonHCoc.getText(), Integer.parseInt(txtSOTiet.getText()));
                list.suaMonHoc(mh);
            } catch (IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(this, ex);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
            txtMaMH.setEnabled(true);
            btnSua.setText("Sửa");
        } else {
            JOptionPane.showMessageDialog(this, "Hãy chọn một bảng !");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        DefaultTableModel dtmodel = (DefaultTableModel) table_info.getModel();
        if (btnXoa.getText() == "Xóa") {
            txtMaMH.setEnabled(false);
            btnXoa.setText("Hủy Chế Độ Xóa");
        } else {
            if (table_info.getSelectedRowCount() > 0) {
                MonHoc mh;
                try {
                    int r = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ? ", "Hộp Thoại", JOptionPane.YES_NO_OPTION);
                    if (r == JOptionPane.YES_OPTION) {
                        mh = new MonHoc(txtMaMH.getText(), txtTenMonHCoc.getText(), Integer.parseInt(txtSOTiet.getText()));
                        list.xoaMonHoc(mh);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(JF_QuanLyKhoa.class.getName()).log(Level.SEVERE, null, ex);
                }
                txtMaMH.setEnabled(true);
                btnXoa.setText("Xóa");
            } else {
                JOptionPane.showMessageDialog(this, "Hãy chọn một bảng !");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimMonHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimMonHocActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) table_info.getModel();
        model.setRowCount(0);
        if (list.timKiem(txtMaMH.getText().trim()) == true) {
            for (MonHoc mh : list.listMh) {
                String[] array = new String[]{mh.getMaMonHoc(), mh.getTenMonHoc(), String.valueOf(mh.getSoTiet())};
                model.addRow(array);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã môn học");
        }

    }//GEN-LAST:event_btnTimMonHocActionPerformed

