    public void hienThi() {
        DefaultTableModel model = (DefaultTableModel) table_info.getModel();
        list.hienThiList();
        model.setRowCount(0);
        for (MonHoc monHoc : list.listMh) {
            String[] array = new String[]{monHoc.getMaMonHoc(), monHoc.getTenMonHoc(), String.valueOf(monHoc.getSoTiet())};
            model.addRow(array);
        }
    }

