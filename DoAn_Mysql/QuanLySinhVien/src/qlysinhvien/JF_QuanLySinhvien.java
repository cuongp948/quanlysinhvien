    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed
        DefaultTableModel model = (DefaultTableModel) table_info.getModel();
        Boolean ketQua = true;
        String gioiTinh = "";
        String maKhoa = qlyKhoa.layMaKhoa(cboTenKhoa.getSelectedItem().toString());
        if (rbtnNam.isSelected() == true) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nữ";
        }
        try {
            SinhVien sv = new SinhVien(txtMaSV.getText(), txtHoTen.getText(), gioiTinh,  spdf.format(ngaySinh.getDate() ), txtNoiSinh.getText(), txtDiaChi.getText(),   txtQuan.getText(), maKhoa , Integer.parseInt(txtHocBong.getText()));
            for (int i = 0; i < model.getRowCount(); i++) {
                if (model.getValueAt(i, 1).equals(txtMaSV.getText())) {
                    ketQua = false;
                    break;
                }
            }
            if (ketQua == false) {
                JOptionPane.showMessageDialog(this, "Trùng Mã Sinh Viên");
            } else {
                qlySinhVien.themSinhVien(sv);
                hienThi();
            }
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btn_themActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel) table_info.getModel();
        int d = table_info.getSelectedRow();
        String gioiTinh = "";
        if (rbtnNam.isSelected() == true) {
            gioiTinh = "Nam";
        } else {
            gioiTinh = "Nu";
        }
        String maKhoa = "";
        try {
            if (btn_sua.getText().equals("Sửa")) {
                btn_sua.setText("Chế Độ Sửa");
                txtMaSV.setEnabled(false);
                cboTenKhoa.setEnabled(false);
                btn_them.setEnabled(false);
                btn_xoa.setEnabled(false);
            } else {
                SinhVien sv = new SinhVien(txtMaSV.getText(), txtHoTen.getText(), gioiTinh, spdf.format(ngaySinh.getDate()), txtNoiSinh.getText(), txtDiaChi.getText(), txtQuan.getText(),   qlyKhoa.layMaKhoa(cboTenKhoa.getSelectedItem().toString()), Integer.parseInt(txtHocBong.getText()));
                int j = JOptionPane.showConfirmDialog(this, "Do you want edition ? ", "Confirm",
                        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (j == JOptionPane.YES_OPTION) {
                    if (table_info.getSelectedRowCount() > 0) {
                        JOptionPane.showMessageDialog(null,  qlyKhoa.layMaKhoa(cboTenKhoa.getSelectedItem().toString()));
                        qlySinhVien.suaSinhVien(sv);
                        hienThi();
                    } else {
                        JOptionPane.showMessageDialog(this, "Hãy chọn một bảng !!");
                    }
                    btn_sua.setText("Sửa");
                    txtMaSV.setEnabled(true);
                    btn_them.setEnabled(true);
                    btn_xoa.setEnabled(true);
                    cboTenKhoa.setEnabled(true);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(JF_QuanLySinhvien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_suaActionPerformed
    public void hienThi() {
        DefaultTableModel modelTable = (DefaultTableModel) table_info.getModel();
        modelTable.setRowCount(0);
        qlySinhVien.hienThiList();
        try {
            //SinhVien sv1 = new SinhVien(txt_maSV.getText(),txt_hoTen.getText(),txt_Lop.getText());
            String[] array = new String[]{};
            for (SinhVien sv : qlySinhVien.list) {
                array = new String[]{sv.getMaSV(), sv.getHoTen(), sv.getGioiTinh(), sv.getNgaySinh(), sv.getNoiSinh(), sv.getDiaChi(), sv.getQuan(), sv.getMaKhoa(), String.valueOf(sv.getHocBong())};
                modelTable.addRow(array);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "" + ex);
        }

    }
