    private void btnTimSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSVActionPerformed

        DefaultTableModel modelTable = (DefaultTableModel) table_info.getModel();
        try {
            if (qlySinhVien.timKiem(txtMaSV.getText()) == true) {
                for (SinhVien sinhVien : qlySinhVien.list) {
                    String[] array = new String[]{sinhVien.getMaSV(), sinhVien.getHoTen(), sinhVien.getGioiTinh(), sinhVien.getNgaySinh(), sinhVien.getNoiSinh(),
                        sinhVien.getDiaChi(), sinhVien.getQuan(), sinhVien.getMaKhoa(), String.valueOf(sinhVien.getHocBong())};
                    modelTable.addRow(array);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

    }//GEN-LAST:event_btnTimSVActionPerformed

